    public void getPartidoAlta(Partido partido) {
        logica.anadirPartido(partido);
        mostrarLista();
    } 
    
    public void mostrarLista() {
        refrescarPantalla();
        DefaultTableModel dtm = this.devolverModeloTablaPartidos();
        dtm.setColumnIdentifiers(new String[] {"Equipo local", "Equipo visitante", "Divisi√≥n", "Resultado", "Fecha"});
        for (Partido partido : listaPartidos) {
            dtm.addRow(this.conseguirObjeto(partido));
        }
    }
    
    public Object[] conseguirObjeto(Partido partido){
        Object[] arrayObjetos = new Object[5];

        arrayObjetos[0] = partido.getEquipoLocal();
        arrayObjetos[1] = partido.getEquipoVisitante();
        arrayObjetos[2] = partido.getDivisionPartido();
        arrayObjetos[3] = partido.getResultado();
        arrayObjetos[4] = partido.getFecha();
        
        return arrayObjetos;
    }
    
    private void refrescarPantalla(){
        DefaultTableModel dtm = this.devolverModeloTablaPartidos();
        dtm.setRowCount(0);
    }
    
    private DefaultTableModel devolverModeloTablaPartidos(){
        DefaultTableModel dtm = new DefaultTableModel();
        dtm = (DefaultTableModel) jTableTablaPartidos.getModel();
        return dtm;
    }

