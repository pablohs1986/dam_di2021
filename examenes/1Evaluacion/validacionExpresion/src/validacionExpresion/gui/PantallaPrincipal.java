    private boolean validarExpresion(String expresionAValidar) {
        String patron = "(\\d+[+-/*]\\d+)(([+-/*]\\d+)+)?";
        Pattern pattern = Pattern.compile(patron);
        Matcher matcher = pattern.matcher(expresionAValidar);
        return matcher.matches();
    }

