    private void botonCalculadoraTeclaIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCalculadoraTeclaIgualActionPerformed
        String operacionAValidar = jTextFieldResultado.getText();
        // Valido usando método propio (está debajo de los actionPerformed)
        if (validarExpresion(operacionAValidar)) {
            // Si valida, asigno valores a la expresión que voy a almacenar
            Expresion expresion = new Expresion();
            Date fecha = new Date();
            SimpleDateFormat formateadorHora = new SimpleDateFormat("HH:mm:ss");
            String hora = formateadorHora.format(fecha);

            expresion.setFecha(fecha);
            expresion.setHora(hora);
            expresion.setOperacion(operacionAValidar);

            // Añado a la lista y muestro el mensaje de confirmación
            LogicaNegocio.aniadirExpresion(expresion);
            JOptionPane.showMessageDialog(this, "Se procede a guardar en la lista de operaciones", "Expresión correcta", JOptionPane.INFORMATION_MESSAGE);

            // Limpio el campo para dejarlo listo para la siguiente operación
            jTextFieldResultado.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Expresión incorrecta", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonCalculadoraTeclaIgualActionPerformed

