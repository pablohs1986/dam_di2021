    private void cargarAyuda() throws HelpSetException, MalformedURLException {
        // Carga el fichero de ayuda
        File fichero = new File("./help/help_set.hs");
        URL hsURL = fichero.toURI().toURL();
        // Crea el HelpSet y el HelpBroker
        HelpSet helpset = new HelpSet(getClass().getClassLoader(), hsURL);
        HelpBroker hb = helpset.createHelpBroker();
        // Pone ayuda a item de menu al pulsarlo.
        hb.enableHelpOnButton(botonCalculadoraTeclaAyuda, "ventana_principal", helpset);    // Botón ayuda en pantalla principal
        hb.enableHelpKey(this.getContentPane(), "ventana_principal", helpset);              // Tecla f1 en pantalla principal
        hb.enableHelpKey(pantallaLista.getContentPane(), "ventana_lista", helpset);         // Tecla F1 en pantalla lista

    }

